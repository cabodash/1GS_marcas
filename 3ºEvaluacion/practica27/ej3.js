var numeroPedido=prompt("Introduce un número entre el 1 y el 7");
switch (numeroPedido){
    case "1":
        alert("Lunes");
        break;
    case "2":
        alert("Lunes");
        break;
    case "3":
        alert("Miércoles");
        break;
    case "4":
        alert("Jueves");
        break;
    case "5":
        alert("Viernes");
        break;
    case "6":
        alert("Sábado");
        break;
    case "7":
        alert("Domingo");
        break;
    default:
        alert("El número es mayor que 7 o menor que 1");
        break;
}
